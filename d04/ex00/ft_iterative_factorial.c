int		ft_iterative_factorial(int nb)
{
	int fac;

	fac = 1;
	if (nb > 12 || nb < 0)
	{
		return (0);
	}
	else
	{
		while (nb > 0)
		{
			fac *= nb;
			nb--;
		}
	}
	return (fac);
}
