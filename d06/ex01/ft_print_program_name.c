int		ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int		main(int argc, char **argv)
{
	argc = -argc;
	ft_putstr(argv[0]);
	ft_putchar('\n');
	return (0);
}
